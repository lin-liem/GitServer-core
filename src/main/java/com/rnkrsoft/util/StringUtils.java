package com.rnkrsoft.util;


import java.io.IOException;
import java.io.PrintWriter;
import java.io.Writer;

/**
 * 字符串工具
 */
public abstract class StringUtils {
    public static final String CLASSPATH_ALL_URL_PREFIX = "classpath*:";
    public static final String CLASSPATH_URL_PREFIX = "classpath:";

    /**
     * 驼峰风格命名转换成下划线风格的命名
     *
     * @param str 驼峰命名风格字符串
     * @return 下划线风格的命名
     */
    public static String camelToUnderline(String str) {
        if (isBlank(str)) {
            return "";
        }
        int len = str.length();
        StringBuilder sb = new StringBuilder(len);
        for (int i = 0; i < len; i++) {
            char c = str.charAt(i);
            if (Character.isUpperCase(c) && i > 0) {
                sb.append("_");
                sb.append(Character.toUpperCase(c));
            } else {
                sb.append(Character.toUpperCase(c));
            }
        }
        return sb.toString();
    }

    /**
     * 下划线风格的命名转换成驼峰风格命名
     *
     * @param str 下划线的命名
     * @return 驼峰命名
     */
    public static String underlineToCamel(String str) {
        if (isBlank(str)) {
            return "";
        }
        int len = str.length();
        StringBuilder sb = new StringBuilder(len);
        str = str.toLowerCase();
        String[] words = str.split("_");
        for (int i = 0; i < words.length; i++) {
            if (i == 0) {
                sb.append(words[i]);
            } else {
                sb.append(firstCharToUpper(words[i]));
            }
        }
        return sb.toString();
    }

    /**
     * 首字母大写
     *
     * @param str 字符串
     * @return 字符串
     */
    public static String firstCharToUpper(String str) {
        if (isBlank(str)) {
            return "";
        }
        char[] chars = str.toCharArray();
        chars[0] = Character.toUpperCase(chars[0]);
        return new String(chars);
    }

    /**
     * 首字母小写
     *
     * @param str 字符串
     * @return 字符串
     */
    public static String firstCharToLower(String str) {
        if (isBlank(str)) {
            return "";
        }
        char[] chars = str.toCharArray();
        chars[0] = Character.toLowerCase(chars[0]);
        return new String(chars);
    }

    public static boolean isBlank(Object val) {
        return val == null || val.toString().isEmpty();
    }

    public static boolean isNotBlank(Object val) {
        return val != null && !val.toString().isEmpty();
    }

    /**
     * 填充字符串长度
     *
     * @param in            输入字符串
     * @param leftFillStyle 是否进行左填充
     * @param fill          填充字符
     * @param len           长度
     * @return 期望长度的字段
     */
    public static String fill(String in, boolean leftFillStyle, char fill, int len) {
        String str = in;
        while (str.length() < len) {
            str = leftFillStyle ? fill + str : str + fill;
        }
        return str;
    }

    /**
     * 以安全的方式获取字符串
     *
     * @param in           输入对象
     * @param ifNullRetVal 如果为空，则返回设置的值
     * @return 字符串
     */
    public static String safeToString(Object in, String ifNullRetVal) {
        if (in == null) {
            return ifNullRetVal;
        }
        return in.toString();
    }

    /**
     * 以安全的方式获取字符串
     *
     * @param in 输入对象
     * @return 获取字符串形式
     */
    public static String safeToString(Object in) {
        return safeToString(in, "");
    }

    /**
     * 使用{}占位符的字符串，并进行替换
     *
     * @param format 带占位符的字符串
     * @param args   参数
     * @return 替换完成的信息
     */
    public static String format(String format, Object... args) {
        return MessageFormatter.format(format, args);
    }

    /**
     * 获取包名的缩写
     * @param packageName 包名
     * @param nameLength 每个单词的最大长度
     * @param firstCharToUpper 首字母是否大写
     * @param splitChar 分割字符
     * @return 短包名
     */
    public static String shortPackageName(final String packageName, final int nameLength, final boolean firstCharToUpper, final String splitChar) {
        if (packageName == null) {
            return packageName;
        }
        StringBuilder builder = new StringBuilder();
        String[] values = packageName.split("\\.");
        for (String value : values) {
            if (value.length() > 1) {
                String str = value.substring(0, value.length() < nameLength ? value.length() : nameLength);
                if (firstCharToUpper) {
                    str = firstCharToUpper(str);
                } else {
                    str = firstCharToLower(str);
                }
                builder.append(str).append(splitChar);
            }
        }
        builder.delete(builder.length() - splitChar.length(), builder.length());
        return builder.toString();
    }

    /**
     * 缩写类名
     *
     * @param fullClassName 类全限定名
     * @param nameLength 名字长度
     * @param firstCharToUpper 首字母是否大写
     * @param splitChar 分割符
     * @return 短包的类名
     */
    public static String shortClassName(String fullClassName, int nameLength, boolean firstCharToUpper, String splitChar) {
        int lastDotIndex = fullClassName.lastIndexOf(".");
        String packageName = fullClassName.substring(0, lastDotIndex);
        String className = fullClassName.substring(lastDotIndex + 1);
        return shortPackageName(packageName, nameLength, firstCharToUpper, splitChar) + splitChar + className;
    }


    /**
     * 删除classpath*:前的星号前缀
     * @param path classpath*:
     * @return 文件路径
     */
    public static String deleteClassPathStar(String path) {
        String temp = path;
        int classpathPos = temp.indexOf(CLASSPATH_ALL_URL_PREFIX);
        if (classpathPos > -1) {
            temp = path.substring(classpathPos + CLASSPATH_ALL_URL_PREFIX.length());
        }
        return temp;
    }

    /**
     * 删除classpath:前的前缀
     * @param path classpath:
     * @return 文件路径
     */
    public static String deleteClassPath(String path) {
        String temp = path;
        int classpathPos = temp.indexOf(CLASSPATH_URL_PREFIX);
        if (classpathPos > -1) {
            temp = path.substring(classpathPos + CLASSPATH_URL_PREFIX.length());
        }
        return temp;
    }

    /**
     * 将文件路径转换为java包路径
     * @param path 文件路径
     * @return java包路径
     */
    public static String convertPathToPackage(String path) {
        String filePath = path;
        String suffix = "";
        int fileNamePos = path.lastIndexOf(".");
        if (fileNamePos > -1) {
            filePath = path.substring(0, fileNamePos).replaceAll("/", ".");
            suffix = path.substring(fileNamePos + 1);
        }
        String file = filePath + "." + suffix;
        return file;
    }


    /**
     * 将Java包路径转换为文件路径
     * @param path Java包路径
     * @return 文件路径
     */
    public static String convertPackageToPath(String path) {
        String filePath = path;
        String suffix = "";
        int fileNamePos = path.lastIndexOf(".");
        if (fileNamePos > -1) {
            filePath = path.substring(0, fileNamePos).replaceAll("\\.", "\\/");
            suffix = path.substring(fileNamePos + 1);
        }
        String file = filePath + "." + suffix;
        return file;
    }
    /**
     * 根据输入的类全限定名获取包名
     * @param clazz 类对象
     * @return 包名路径
     */
    public static String getPackage(Class clazz){
        String className = clazz.getName();
        int idx = className.lastIndexOf(".");
        if (idx > -1){
            return className.substring(0, idx);
        }else{
            return "";
        }
    }

    /**
     * 根据输入的类全限定名获取包名
     * @param className 类全限定名
     * @return 包名路径
     */
    public static String getPackage(String className){
        int idx = className.lastIndexOf(".");
        if (idx > -1){
            return className.substring(0, idx);
        }else{
            return "";
        }
    }

    /**
     * 将异常对象输出为字符串形式
     * @param e 异常
     * @return 异常字符串形式
     */
    public static String toString(Throwable e) {
        UnsafeStringWriter w = new UnsafeStringWriter();

        PrintWriter p = new PrintWriter(w);
        try {
            e.printStackTrace(p);
            return w.toString();
        } finally {
            p.close();
        }
    }

    /**
     * 将异常对象输出为字符串形式
     * @param message 异常字符串
     * @param e 异常
     * @return 异常字符串形式
     */
    public static String toString(String message, Throwable e) {
        UnsafeStringWriter w = new UnsafeStringWriter();
        w.write(message + "\n");
        PrintWriter p = new PrintWriter(w);
        try {
            e.printStackTrace(p);
            return w.toString();
        } finally {
            p.close();
        }
    }

    static class UnsafeStringWriter extends Writer {
        private StringBuilder mBuffer;

        public UnsafeStringWriter() {
            lock = mBuffer = new StringBuilder();
        }

        public UnsafeStringWriter(int size) {
            if (size < 0)
                throw new IllegalArgumentException("Negative buffer size");

            lock = mBuffer = new StringBuilder();
        }

        @Override
        public void write(int c) {
            mBuffer.append((char) c);
        }

        @Override
        public void write(char[] cs) throws IOException {
            mBuffer.append(cs, 0, cs.length);
        }

        @Override
        public void write(char[] cs, int off, int len) throws IOException {
            if ((off < 0) || (off > cs.length) || (len < 0) ||
                    ((off + len) > cs.length) || ((off + len) < 0))
                throw new IndexOutOfBoundsException();

            if (len > 0)
                mBuffer.append(cs, off, len);
        }

        @Override
        public void write(String str) {
            mBuffer.append(str);
        }

        @Override
        public void write(String str, int off, int len) {
            mBuffer.append(str.substring(off, off + len));
        }

        @Override
        public Writer append(CharSequence csq) {
            if (csq == null)
                write("null");
            else
                write(csq.toString());
            return this;
        }

        @Override
        public Writer append(CharSequence csq, int start, int end) {
            CharSequence cs = (csq == null ? "null" : csq);
            write(cs.subSequence(start, end).toString());
            return this;
        }

        @Override
        public Writer append(char c) {
            mBuffer.append(c);
            return this;
        }

        @Override
        public void close() {
        }

        @Override
        public void flush() {
        }

        @Override
        public String toString() {
            return mBuffer.toString();
        }
    }
}
